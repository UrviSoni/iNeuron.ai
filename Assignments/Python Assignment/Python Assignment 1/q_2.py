# Write a Python program to accept the user's first and last name and then getting them
# printed in the the reverse order with a space between first name and last name.

fname = input("Your First Name: ") # Taking 'fname' as an input
lname = input("Your Last Name: ") # Taking 'lname' as an input
print(lname + " " + fname) # printing them in the reverse order with a space between 'fname' and 'lname'